                        <p>O <b>Realismo</b> e o <b>Naturalismo</b> são dois movimentos artístico-culturais bastante similares. O Naturalismo, no entanto, é considerado uma "radicalização" do Realismo, pois mostra o ser humano como um "produto" da natureza, toda a sua agressividade, necessidades fisiológicas, taras e manias sexuais, entre outras características animalescas dão forma as narrativas e personagens.</p>
