                        <p>Nesse momento, os escritores assumem uma posição mais crítica em relação à sociedade e aos modelos literários anteriores.</p>
                        <p>Por isso, muitos escritores pré modernos rompem com a linguagem formal do arcadismo e, além disso, exploram temas históricos, políticos e econômicos. Isso porque o Brasil passava por momentos como a República do café com leite e inúmeras revoltas (Revolta da Vacina, Revolta da Chibata, Revolta da Armada, Revolta de Canudos).</p>
                        <p>Os Pré Modernistas que se destacaram na prosa foram: Euclides da Cunha, Graça Aranha, Monteiro Lobato e Lima Barreto.</p> <br>

                        <h6><b> Euclides da Cunha (1866-1909) </b></h6>

                        <p>Euclides Rodrigues da Cunha foi um escritor, poeta, ensaísta, jornalista, historiador, sociólogo, geógrafo, poeta e engenheiro brasileiro. Ocupou a cadeira 7 na Academia Brasileira de Letras de 1903 a 1906.</p>
                        <p>Publicou "Os Sertões: Campanha de Canudos" em 1902, a qual é dividida em três partes: A Terra, o Homem, A Luta. A obra regionalista retrata a vida do sertanejo. Publicou também a Guerra de Canudos (1896-1897) no interior da Bahia.</p> <br>

                        <h6><b> Graça Aranha (1868-1931) </b></h6>

                        <p>José Pereira da Graça Aranha foi um escritor e diplomata maranhense. Um dos fundadores da Academia Brasileira de Letras e um dos organizadores da Semana de Arte Moderna de 1922.</p>
                        <p>Sua obra que merece destaque é "Canaã" publicada em 1902. A obra aborda a migração alemã no estado do Espírito Santo. Outras obras que merecem destaque: Malazarte (1914), A Estética da Vida (1921) e Espírito Moderno (1925).</p> <br>

                        <h6><b> Monteiro Lobato (1882-1948) </b></h6>

                        <p>José Bento Renato Monteiro Lobato foi um escritor, editor, ensaísta e tradutor brasileiro. Um dos mais influentes escritores do século XX, Monteiro Lobato ficou muito conhecido por suas obras infantis de caráter educativo, como por exemplo, a série de livros do Sítio do Pica-Pau Amarelo.</p>
                        <p>Em 1918 publica "Urupês" uma coletânea regionalista de contos e crônicas. Já em 1919 publica "Cidades Mortas" livro de contos que retrata a queda do Ciclo do Café.</p> <br>

                        <h6><b> Lima Barreto (1881-1922) </b></h6>

                        <p>Afonso Henriques de Lima Barreto, conhecido como Lima Barreto, foi um escritor e jornalista brasileiro. Autor de uma obra crítica veiculada aos temas sociais, o escritor rompe com o nacionalista ufanista e faz críticas ao positivismo.</p>
                        <p>Sua obra que merece destaque é o "Triste Fim de Policarpo Quaresma" escrito numa linguagem coloquial. Nela o autor faz crítica à sociedade urbana da época.</p> 

