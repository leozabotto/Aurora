                        <p>Em Portugal, o Classicismo compreende o período literário do século XVI (entre 1537 e 1580). O marco inicial do movimento foi a chegada do poeta Francisco Sá de Miranda à Portugal.</p>
                        <p>Ali, ele se inspirou no humanismo italiano, trazendo uma nova forma de poesia: o “dolce stil nuevo” (Doce estilo novo).</p>
                        <p>Esse novo modelo estava baseado na forma fixa do soneto (2 quartetos e 2 tercetos), nos versos decassílabos e na oitava rima.</p>
                        <p>Além de Sá de Miranda merecem destaque os escritores portugueses classicistas:</p>
