                        <p>Na idade Média, período que durou dez séculos (V ao XV), o principal atributo da sociedade era a religião.</p>
                        <p>Esse momento esteve marcado pelo teocentrismo, cujo lema eram os dogmas e preceitos da Igreja Católica, que cada vez mais adquiria fiéis.</p>
                        <p>Assim, pessoas que estivessem contra ou questionassem esses dogmas, eram excomungados, além de sofrer alijamento da sociedade, ou em último caso, a morte.</p>
                        <p>O humanismo, que surgiu a partir do século XV na Europa, começou a questionar diversas questões uma vez que o cientificismo despontava.</p>
                        <p>Muitos estudiosos foram capazes de propor novas formas de análise do mundo e da vida, que estivessem além do divino. Ou seja, apresentavam questões baseadas na racionalidade humana e no antropocentrismo (homem no centro do mundo).</p>
                        <p>Esse momento esteve marcado por grandes transformações e descobertas históricas:</p>
                        <ul class="collection">
                            <li class="collection-item">as Grandes Navegações;</li>
                            <li class="collection-item">a Reforma Protestante (que levou a uma crise religiosa) encabeçada por Martinho Lutero;</li>
                            <li class="collection-item">a invenção da Imprensa pelo alemão Gutenberg;</li>
                            <li class="collection-item">o fim do sistema feudal (início do capitalismo);</li>
                            <li class="collection-item">o cientificismo de Copérnico e Galileu.</li>
                        </ul>
                        <p>Foi nesse contexto que as pessoas buscavam novas expressões artísticas pautadas no equilíbrio clássico.</p>
                        <p>Assim, surgiu o renascimento cultural, período de grandes transformações artísticas, culturais, políticas e que espalhou-se por todo o continente europeu.</p>
