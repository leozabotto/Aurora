                        <p>No entanto, foi a partir de Luís de Camões, um dos maiores poetas portugueses e da literatura mundial, que a literatura portuguesa ganha notoriedade.</p>
                        <p>Sua grande obra "Os Lusíadas” (1572), é uma epopeia classicista onde ele narra a viagem de Vasco da Gama às Índias. Ela foi escrita em 10 cantos e está composta de 8816 versos decassílabos em oitava rima distribuídos em 1120 estrofes.</p>
                        <p>O Classicismo em Portugal permaneceu até 1580. Esse é o ano da morte de Camões e também da União das Coroas Ibéricas, aliança estabelecida até 1640 entre Espanha e Portugal.</p>
                        <p>Obs: No Brasil, esse período literário ficou conhecido como Quinhentismo.</p>
